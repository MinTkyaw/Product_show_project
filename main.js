// 🔁 Load saved language on page load
document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("language") || "en";
  document.documentElement.lang = savedLang;

  // Update translatable text
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    el.textContent = translations[savedLang][key];
  });

  // Update placeholder text
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    el.placeholder = translations[savedLang][key];
  });

  // Update language switch button text
  document.getElementById("langSwitch").textContent =
    savedLang === "en" ? "Myanmar" : "English";

  // Adjust font size if needed
  updateFontSizeForLanguage(savedLang);
});

// 🔁 Handle language switch button click
document.getElementById("langSwitch").addEventListener("click", () => {
  const currentLang = document.documentElement.lang;
  const newLang = currentLang === "en" ? "my" : "en";
  document.documentElement.lang = newLang;

  // Save new language
  localStorage.setItem("language", newLang);

  // Update button text
  document.getElementById("langSwitch").textContent =
    newLang === "en" ? "Myanmar" : "English";

  // Update translatable content
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    el.textContent = translations[newLang][key];
  });

  // Update placeholders
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    el.placeholder = translations[newLang][key];
  });

  // Adjust font size if needed
  updateFontSizeForLanguage(newLang);
});

// ✅ Font size and line-height for Myanmar language
function updateFontSizeForLanguage(lang) {
  const heroTitle = document.getElementById("hero-title");
  const aboutTitle = document.getElementById("about-title");
  const featureTitle = document.getElementById("feature-title");
  const featureQualityTitle = document.getElementById("feature-quality-title");
  const featureColorTitle = document.getElementById("feature-color-title");
  const featureHandmadeTitle = document.getElementById(
    "feature-handmade-title"
  );
  const featureEcoTitle = document.getElementById("feauter-eco-title");
  const featureSoftTitle = document.getElementById("feature-soft-title");
  const featureFitTitle = document.getElementById("feature-fit-title");
  const productTitle = document.getElementById("products-title");
  const contactTitle = document.getElementById("contact-title");
  if (!heroTitle) return;

  if (lang === "my") {
    heroTitle.classList.add("myanmar-font-size");
    aboutTitle?.classList.add("myanmar-about-size");
    featureTitle?.classList.add("myanmar-features-header-size");
    featureQualityTitle?.classList.add("myanmar-feature-title-size");
    featureColorTitle?.classList.add("myanmar-feature-title-size");
    featureHandmadeTitle?.classList.add("myanmar-feature-title-size");
    featureEcoTitle?.classList.add("myanmar-feature-title-size");
    featureSoftTitle?.classList.add("myanmar-feature-title-size");
    featureFitTitle?.classList.add("myanmar-feature-title-size");
    productTitle?.classList.add("myanmar-features-header-size");
    contactTitle?.classList.add("myanmar-about-size");
  } else {
    heroTitle.classList.remove("myanmar-font-size");
    aboutTitle?.classList.remove("myanmar-about-size");
    featureTitle?.classList.remove("myanmar-features-header-size");
    featureQualityTitle?.classList.remove("myanmar-feature-title-size");
    featureColorTitle?.classList.remove("myanmar-feature-title-size");
    featureHandmadeTitle?.classList.remove("myanmar-feature-title-size");
    featureEcoTitle?.classList.remove("myanmar-feature-title-size");
    featureSoftTitle?.classList.remove("myanmar-feature-title-size");
    featureFitTitle?.classList.remove("myanmar-feature-title-size");
    productTitle?.classList.remove("myanmar-features-header-size");
    contactTitle?.classList.remove("myanmar-about-size");
  }
}

